import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

function extractProducts(source) {
  const start = source.indexOf('export const products: Product[] = [');
  const end = source.indexOf('export interface ProductPreviewPair');
  const block = source.slice(start, end);
  const entries = [...block.matchAll(/id:\s*'([^']+)'[\s\S]*?name:\s*'([^']+)'/g)];
  return entries.map((match) => ({ id: match[1], name: match[2] }));
}

const productsSource = readFileSync(join(root, 'src/data/products.ts'), 'utf8');
const products = extractProducts(productsSource);

const tokenToPath = new Map();

for (const product of products) {
  const path = `/dolls/${product.id}-sex-doll-nz`;
  const tokens = new Set([
    product.id.toLowerCase(),
    product.name.toLowerCase(),
    product.name.toLowerCase().replace(/\s+/g, '-'),
  ]);

  for (const token of tokens) {
    if (!token || token.length < 2) continue;
    const existing = tokenToPath.get(token);
    if (!existing || token.length > existing.token.length) {
      tokenToPath.set(token, { path, token });
    }
  }
}

const sortedTokens = [...tokenToPath.entries()].sort((a, b) => b[0].length - a[0].length);
const mapObject = Object.fromEntries(sortedTokens.map(([token, value]) => [token, value.path]));

const file = `// Auto-generated by scripts/generate-woo-redirect-map.mjs — do not edit manually.
export const wooProductTokenMap: Record<string, string> = ${JSON.stringify(mapObject, null, 2)};
`;

writeFileSync(join(root, 'src/data/wooProductRedirectMap.ts'), file, 'utf8');
console.log(`Generated Woo redirect map with ${sortedTokens.length} product tokens.`);
